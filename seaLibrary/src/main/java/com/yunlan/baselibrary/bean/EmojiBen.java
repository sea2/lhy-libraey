public class EmojiBen {
    int imgId;
    String key;

    public EmojiBen(int id, String key) {
        this.imgId = id;
        this.key = key;
    }

    public int getImgId() {
        return imgId;
    }

    public void setImgId(int imgId) {
        this.imgId = imgId;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }





}
